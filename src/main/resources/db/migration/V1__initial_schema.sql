CREATE TABLE IF NOT EXISTS user_addresses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cep VARCHAR(9) NOT NULL,
  street VARCHAR(90) NOT NULL,
  number VARCHAR(12) NOT NULL,
  neighborhood VARCHAR(50) NOT NULL,
  complement VARCHAR(50),
  city VARCHAR(50) NOT NULL,
  state VARCHAR(2) NOT NULL
);

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(80) NOT NULL,
  phone VARCHAR(20),
  email VARCHAR(120) NOT NULL UNIQUE,
  password_hash VARCHAR(255) NOT NULL,
  address_id BIGINT REFERENCES user_addresses(id),
  role VARCHAR(20) NOT NULL,
  accepts_marketing BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS candle_product (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(60) NOT NULL,
  scent VARCHAR(60) NOT NULL,
  description VARCHAR(180) NOT NULL,
  price NUMERIC(10, 2) NOT NULL,
  stock INTEGER NOT NULL,
  color VARCHAR(30) NOT NULL,
  size VARCHAR(20) NOT NULL DEFAULT '120g',
  occasion VARCHAR(30) NOT NULL DEFAULT 'classica',
  mood VARCHAR(30) NOT NULL DEFAULT 'aconchegante',
  active BOOLEAN NOT NULL DEFAULT TRUE,
  image_url TEXT,
  extra_image_url_one TEXT,
  extra_image_url_two TEXT
);

CREATE TABLE IF NOT EXISTS customer_orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES users(id),
  delivery_address VARCHAR(160) NOT NULL,
  payment_method VARCHAR(30) NOT NULL,
  status VARCHAR(30) NOT NULL DEFAULT 'CREATED',
  total NUMERIC(10, 2) NOT NULL DEFAULT 0,
  coupon_code VARCHAR(30),
  discount_total NUMERIC(10, 2) NOT NULL DEFAULT 0,
  shipping_cep VARCHAR(12),
  shipping_cost NUMERIC(10, 2) NOT NULL DEFAULT 0,
  shipping_days INTEGER,
  payment_simulation VARCHAR(30),
  email_notification VARCHAR(220),
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS order_item (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id BIGINT NOT NULL REFERENCES customer_orders(id),
  product_id BIGINT NOT NULL REFERENCES candle_product(id),
  quantity INTEGER NOT NULL,
  unit_price NUMERIC(10, 2) NOT NULL
);
